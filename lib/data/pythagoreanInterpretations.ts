// A helper function for retrieving data, similar to your getCellInfo
export function getInterpretation(
  cellId: string,
  cellValue: string
): Interpretation {
  const id = cellId as CellKey;
  const value = cellValue === '—' ? '—' : cellValue;

  const title = cellTitles[id] || 'Описание';
  const description =
    (interpretations[id] && interpretations[id][value]) ||
    '<p>Подробное описание для этого значения пока не добавлено.</p>';

  return { title, description };
}
